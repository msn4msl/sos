public class SearchDetails {
    
    private int Itemid;
    private String ItemName;
    private String OrderCode;
    private int OrderNo;
    private String StorehouseNo;
    private int RoomNo;
    private String RackRowNo;

    /**
     * @return the Itemid
     */
    public int getItemid() {
        return Itemid;
    }

    /**
     * @param Itemid the Itemid to set
     */
    public void setItemid(int Itemid) {
        this.Itemid = Itemid;
    }

    /**
     * @return the ItemName
     */
    public String getItemName() {
        return ItemName;
    }

    /**
     * @param ItemName the ItemName to set
     */
    public void setItemName(String ItemName) {
        this.ItemName = ItemName;
    }

    /**
     * @return the OrderCode
     */
    public String getOrderCode() {
        return OrderCode;
    }

    /**
     * @param OrderCode the OrderCode to set
     */
    public void setOrderCode(String OrderCode) {
        this.OrderCode = OrderCode;
    }

    /**
     * @return the OrderNo
     */
    public int getOrderNo() {
        return OrderNo;
    }

    /**
     * @param OrderNo the OrderNo to set
     */
    public void setOrderNo(int OrderNo) {
        this.OrderNo = OrderNo;
    }

    /**
     * @return the StorehouseNo
     */
    public String getStorehouseNo() {
        return StorehouseNo;
    }

    /**
     * @param StorehouseNo the StorehouseNo to set
     */
    public void setStorehouseNo(String StorehouseNo) {
        this.StorehouseNo = StorehouseNo;
    }

    /**
     * @return the RoomNo
     */
    public int getRoomNo() {
        return RoomNo;
    }

    /**
     * @param RoomNo the RoomNo to set
     */
    public void setRoomNo(int RoomNo) {
        this.RoomNo = RoomNo;
    }

    /**
     * @return the RackRowNo
     */
    public String getRackRowNo() {
        return RackRowNo;
    }

    /**
     * @param RackRowNo the RackRowNo to set
     */
    public void setRackRowNo(String RackRowNo) {
        this.RackRowNo = RackRowNo;
    }

   
}
